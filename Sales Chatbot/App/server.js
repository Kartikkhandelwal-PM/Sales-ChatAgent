// Chat (streaming SSE)
app.post('/api/chat', async (req, res) => {
  const { messages, userContext, sessionId } = req.body;
  if (!Array.isArray(messages)) return res.status(400).json({ error: 'Invalid messages' });

  res.setHeader('Content-Type', 'text/event-stream');
  res.setHeader('Cache-Control', 'no-cache');
  res.setHeader('Connection', 'keep-alive');

  // Build context-aware message list
  const contextMessages = [];
  if (userContext && userContext.name) {
    const firstName = userContext.name.split(' ')[0];
    const roleDesc = userContext.role === 'CA / Tax Professional'
      ? 'a CA / Tax Professional who manages compliance for multiple clients (DIFM)'
      : 'a business owner managing their own company compliance (DIY)';
    contextMessages.push({
      role: 'user',
      content: `[System context, do not repeat this to the user: The person I am speaking with is ${userContext.name} (address them as "${firstName}"). They are ${roleDesc}. Their email is ${userContext.email}. Tailor all responses to their profile.]`
    });
    contextMessages.push({
      role: 'assistant',
      content: `[Understood. I will address this person as ${firstName}, pitch the appropriate angle for their role, and guide them toward a demo.]`
    });
  }

  try {
    const lastUserMsg = messages[messages.length - 1]?.content || '';

    // Build a context-enriched query: last message + previous user message for better RAG on follow-ups
    const prevUserMsgs = messages.slice(-6).filter(m => m.role === 'user');
    const prevUserMsg  = prevUserMsgs.length >= 2 ? prevUserMsgs[prevUserMsgs.length - 2].content : '';
    const contextQuery = prevUserMsg ? `${prevUserMsg} ${lastUserMsg}` : lastUserMsg;

    // Retrieve relevant KB articles — use RAG if embeddings loaded, else keyword fallback
    let retrievedKB = '';
    if (embeddedArticles.length > 0) {
      try {
        // First try: embed last message only
        const embRes = await openai.embeddings.create({
          model: 'text-embedding-3-small',
          input: lastUserMsg,
        });
        retrievedKB = retrieveByEmbedding(embRes.data[0].embedding, lastUserMsg);

        // Second try: if no results, embed with conversation context
        if (!retrievedKB && prevUserMsg) {
          const embRes2 = await openai.embeddings.create({
            model: 'text-embedding-3-small',
            input: contextQuery,
          });
          retrievedKB = retrieveByEmbedding(embRes2.data[0].embedding, contextQuery);
        }
      } catch(embErr) {
        console.warn('Embedding call failed, falling back to keyword search:', embErr.message);
        retrievedKB = retrieveByKeyword(contextQuery);
      }
    } else {
      retrievedKB = retrieveByKeyword(contextQuery);
    }

    const stream = await openai.chat.completions.create({
      model: process.env.OPENAI_MODEL || 'gpt-4o-mini',
      messages: [
        { role: 'system', content: buildSystemPrompt(retrievedKB) },
        ...contextMessages,
        ...messages.slice(-24)
      ],
      stream: true,
      temperature: 0.5,
      max_tokens: 500
    });

    let fullResponse = '';
    for await (const chunk of stream) {
      const text = chunk.choices[0]?.delta?.content || '';
      if (text) {
        fullResponse += text;
        res.write(`data: ${JSON.stringify({ text })}\n\n`);
      }
    }
    res.write('data: [DONE]\n\n');
    res.end();

    const userMessage = messages[messages.length - 1]?.content || '';
    saveLog({ sessionId, userContext, userMessage, botResponse: fullResponse });

    if (sessionId) {
      const now = new Date().toISOString();
      if (!sessionStore[sessionId]) {
        sessionStore[sessionId] = { id: sessionId, user: userContext || {}, messages: [], startedAt: now, lastAt: now };
      }
      const sess = sessionStore[sessionId];
      if (userContext?.name && !sess.user?.name) sess.user = userContext;
      const lastTwo = sess.messages.slice(-2);
      if (!lastTwo.some(m => m.role === 'user' && m.content === userMessage)) {
        sess.messages.push({ role: 'user', content: userMessage, ts: now });
        sess.messages.push({ role: 'assistant', content: fullResponse, ts: new Date().toISOString() });
      }
      sess.lastAt = new Date().toISOString();
      saveSession(sess);
    }
  } catch (err) {
    console.error('OpenAI error:', err.message);
    const msg = err.status === 429
      ? 'Too many requests, please wait a moment and try again.'
      : 'Something went wrong. Please try again.';
    res.write(`data: ${JSON.stringify({ error: msg })}\n\n`);
    res.end();
  }
});


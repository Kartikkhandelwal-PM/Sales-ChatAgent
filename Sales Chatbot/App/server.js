// ── Reformulate raw feedback into a clean AI-readable rule ──
async function reformulateCorrection(customerQuestion, botResponse, rawFeedback) {
  try {
    const prompt = `You are helping improve the KDK Software Sales Chatbot for KDK Software (Express GST, TDS, ITR products).

A sales team member reviewed a chatbot response and gave raw feedback, it may be in Hindi, English, or mixed, and may be informal or unstructured.

Your job: Convert their feedback into a clear, actionable correction RULE in English that the AI can follow in future conversations.

The rule must:
- Start with "RULE:"
- Be 2-4 sentences max
- Be specific and actionable (tell the AI exactly what to DO, not just what was wrong)
- Include a short example if helpful
- Be in English regardless of feedback language

Context:
Customer question: ${customerQuestion || '(not provided)'}

What Aria responded (the response being corrected):
${(botResponse || '').slice(0, 500)}

Raw feedback from reviewer:
${rawFeedback}

Write ONLY the rule. No preamble, no explanation.`;

    const result = await openai.chat.completions.create({
      model: process.env.OPENAI_MODEL || 'gpt-4o-mini',
      messages: [{ role: 'user', content: prompt }],
      temperature: 0.3,
      max_tokens: 180
    });
    return result.choices[0]?.message?.content?.trim() || rawFeedback;
  } catch(e) {
    console.error('Reformulation error:', e.message);
    return rawFeedback;
  }
}


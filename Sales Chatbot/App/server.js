function cosineSim(a, b) {
  let dot = 0, normA = 0, normB = 0;
  for (let i = 0; i < a.length; i++) {
    dot   += a[i] * b[i];
    normA += a[i] * a[i];
    normB += b[i] * b[i];
  }
  return dot / (Math.sqrt(normA) * Math.sqrt(normB));
}

function titleBoost(rawQuery, title) {
  // Split query into individual words, find specific identifiers (GSTR6, 26Q, ITR7, 24Q etc.)
  const words = rawQuery.toLowerCase().split(/[^a-z0-9]+/);
  const titleNorm = title.toLowerCase().replace(/[-\s]+/g, '');
  for (const word of words) {
    // Identifier = word with both letters AND digits (e.g. gstr6, 26q, itr7, 24q, 27eq)
    if (word.length >= 2 && /[a-z]/.test(word) && /\d/.test(word)) {
      if (titleNorm.includes(word)) return 0.18;
    }
  }
  return 0;
}

function retrieveByEmbedding(queryEmbedding, rawQuery = '', maxChars = 8000) {
  const scored = embeddedArticles.map(art => ({
    art,
    score: cosineSim(queryEmbedding, art.embedding) + titleBoost(rawQuery, art.title),
  }));
  scored.sort((a, b) => b.score - a.score);

  let result = '';
  for (const { art, score } of scored) {
    if (score < 0.32) break;
    if (result.length + art.content.length + 6 > maxChars) break;
    result += art.content + '\n\n---\n\n';
  }
  return result;
}

function retrieveByKeyword(query, maxChars = 8000) {
  if (!query || allArticles.length === 0) return '';
  const keywords = query.toLowerCase()
    .replace(/[^a-z0-9\s]/g, ' ')
    .split(/\s+/)
    .filter(w => w.length > 2 && !STOPWORDS.has(w));
  if (keywords.length === 0) return '';

  const scored = allArticles.map(art => {
    const titleLower   = art.title.toLowerCase();
    const tagsLower    = art.tags.toLowerCase();
    const contentLower = art.content.toLowerCase();
    const score = keywords.reduce((sum, kw) => sum
      + (titleLower.includes(kw)   ? 5 : 0)
      + (tagsLower.includes(kw)    ? 3 : 0)
      + (contentLower.includes(kw) ? 1 : 0), 0);
    return { art, score };
  });
  scored.sort((a, b) => b.score - a.score);

  let result = '';
  for (const { art, score } of scored) {
    if (score === 0) break;
    if (result.length + art.content.length + 6 > maxChars) break;
    result += art.content + '\n\n---\n\n';
  }
  return result;
}

